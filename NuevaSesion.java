   private boolean validarDatos() {
         String nombreIngresado = nombre.getText().trim();
         String cedulaIngresada = cedula.getText().trim();

         // This is the fix - change hora.getText() to fecha.getText()
         String fechaIngresada = fecha.getText().trim();  // FIXED LINE
         String horaIngresada = hora.getText().trim();

         String especialidadSeleccionada = (String) especialidad.getSelectedItem();
         String entrenadorSeleccionado = (String) entrenador.getSelectedItem();

         //Validar que ningún campo esté vacío
         if (nombreIngresado.isEmpty() || cedulaIngresada.isEmpty() || fechaIngresada.isEmpty() || 
             horaIngresada.isEmpty() || especialidadSeleccionada.equals("Seleccione la especialidad") || 
             entrenadorSeleccionado.equals("Seleccione el entrenador")) {

             JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
             return false;
         }

         // Validar nombre (solo letras y espacios)
         if (!nombreIngresado.matches("^[A-Za-zÁÉÍÓÚáéíóúñÑ ]+$")) {
             JOptionPane.showMessageDialog(this, "El nombre solo debe contener letras y espacios", "Error", JOptionPane.ERROR_MESSAGE);
             return false;
         }

         // Validar fecha (formato dd-MM-yyyy)
         if (!validarFormatoFecha(fechaIngresada, "dd-MM-yyyy")) {
             JOptionPane.showMessageDialog(this, "La fecha debe estar en formato dd-MM-yyyy", "Error", JOptionPane.ERROR_MESSAGE);
             return false;
         }

         // Validar hora (formato HH:mm)
         if (!validarFormatoFecha(horaIngresada, "HH:mm")) {
             JOptionPane.showMessageDialog(this, "La hora debe estar en formato HH:mm", "Error", JOptionPane.ERROR_MESSAGE);
             return false;
         }

         return true; // Si pasa todas las validaciones
    }

    private boolean validarFormatoFecha(String fecha, String formato) {
        SimpleDateFormat sdf = new SimpleDateFormat(formato);
        sdf.setLenient(false); // Evita fechas inválidas como 32-13-2024
        try {
            sdf.parse(fecha);
            return true;
        } catch (ParseException e) {
            return false;
        }
    }
    private static int generarNumeroAleatorio() {
        Random random = new Random();
        return random.nextInt(99999) + 10000; // Genera un número entre 10000 y 99999
    }
     public String[] guardarinfo(){
      if (validarDatos()) {
        sesion[0] = String.valueOf(generarNumeroAleatorio());
        sesion[1] = nombre.getText().trim();
        sesion[2] = cedula.getText().trim();
        sesion[3] = (String) especialidad.getSelectedItem();
        sesion[4] = (String) entrenador.getSelectedItem();
        sesion[5] = fecha.getText().trim();
        sesion[6] = hora.getText().trim();
        return sesion;
      } else{
        return null;
      }
         
    } 
