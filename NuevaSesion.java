    public static void filtrarEntrenadores(String[] ed, String especialidad, JComboBox entrenador){
       entrenador.removeAllItems(); 
       File archivo = new File("entrenadores.txt");
       int i=0;
        try {
            BufferedReader entrada = new BufferedReader(new FileReader(archivo));
            String lectura;
            while((lectura= entrada.readLine())!= null){
                String[] p = lectura.split(",");  
                if((p[2].equals(especialidad)) && Integer.parseInt(p[4])<15){
                    ed[i]= p[0];
                    i++;
                }
            }
            for(int j =0; j< ed.length; j++){
                        if(ed[j]!= null){
                            entrenador.addItem(ed[j]);
                        }
                    }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ManejoArchivo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ManejoArchivo.class.getName()).log(Level.SEVERE, null, ex);
        }
   }

